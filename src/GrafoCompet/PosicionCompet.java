package GrafoCompet;

public class PosicionCompet {
	private int c;
	private int f;
	public PosicionCompet(int f, int c) {
		super();
		this.c = c;
		this.f = f;
	}
	public int getC() {
		return c;
	}
	public int getF() {
		return f;
	}
	public void setC(int c) {
		this.c = c;
	}
	public void setF(int f) {
		this.f = f;
	}
	

}
